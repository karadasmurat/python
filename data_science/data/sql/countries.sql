CREATE TABLE countries (
    country_id INTEGER PRIMARY KEY,
    name TEXT,
    code TEXT
);

CREATE TABLE cities (
    city_id INTEGER PRIMARY KEY,
    name TEXT,
    country_id INTEGER, --FK
    FOREIGN KEY (country_id) REFERENCES countries(country_id)
);

