a = 3 + 2       # Add
a += 10
b = 4 - 1       # Subtract
c = 2 * 3       # Multiply
d1 = 17 / 3     # Divide
d2 = 17 // 3    # Divide - floor division discards the fractional part
e = 2 ** 10     # Exponent
f = 5 % 2       # Modulo - remainder after a division

print(f"{a=} {b=} {c=} {d1=} {d2=} {e=} {f=}") # a=15 b=3 c=6 d1=5.666666666666667 d2=5 e=1024 f=1